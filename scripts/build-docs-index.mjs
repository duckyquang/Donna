// Regenerate the docs search index (site/docs/search-index.js) from the docs pages.
// Run after editing any page in site/docs/:  node scripts/build-docs-index.mjs
import { readFileSync, writeFileSync, readdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const docsDir = join(dirname(fileURLToPath(import.meta.url)), "..", "site", "docs");
const strip = (html) =>
  html.replace(/<[^>]+>/g, " ").replace(/&amp;/g, "&").replace(/&lt;/g, "<").replace(/&gt;/g, ">").replace(/\s+/g, " ").trim();

const entries = [];
for (const file of readdirSync(docsDir).filter((f) => f.endsWith(".html"))) {
  const html = readFileSync(join(docsDir, file), "utf8");
  const pageTitle = strip((html.match(/<h1>([\s\S]*?)<\/h1>/) || [, file])[1]);
  const qaRe = /<div class="qa" id="([^"]+)">([\s\S]*?)<\/div>\s*(?=<div class="qa"|<div class="docs-pager")/g;
  let m;
  while ((m = qaRe.exec(html))) {
    const [, id, body] = m;
    const q = strip((body.match(/<h3>([\s\S]*?)<\/h3>/) || [, ""])[1]);
    entries.push({ page: file, pageTitle, id, q, text: strip(body).slice(0, 400) });
  }
}
writeFileSync(
  join(docsDir, "search-index.js"),
  "// Generated by scripts/build-docs-index.mjs — do not edit by hand.\nwindow.DOCS_INDEX = " +
    JSON.stringify(entries, null, 1) +
    ";\n",
);
console.log(`indexed ${entries.length} entries across ${new Set(entries.map((e) => e.page)).size} pages`);
